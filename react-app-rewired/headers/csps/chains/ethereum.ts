const REACT_APP_ALCHEMY_POLYGON_URL = process.env.REACT_APP_ALCHEMY_POLYGON_URL

if (!REACT_APP_ETHEREUM_NODE_URL) throw new Error('REACT_APP_ETHEREUM_NODE_URL is required')

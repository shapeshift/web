import type { Csp } from '../../types'

const REACT_APP_ETHEREUM_NODE_URL = process.env.REACT_APP_ETHEREUM_NODE_URL
const REACT_APP_UNCHAINED_ETHEREUM_HTTP_URL = process.env.REACT_APP_UNCHAINED_ETHEREUM_HTTP_URL
